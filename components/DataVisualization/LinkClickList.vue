    destroyed() {
      clearInterval(this.timer)//清除计时器
    },
    methods: {
      handleTime() {
        this.time = 15
        this.timer = setInterval(() => {
          if (this.time > 0) {
            this.time--
          } else {
            this.Refresh()
          }
        }, 1000)
      },
      getList() {
        //获取列表信息

        return new Promise(resolve => {
          setTimeout(() => {
            console.log("获取信息")
            resolve(true)
          }, 2000)
        })
      },
      Refresh() {
        clearInterval(this.timer)//清除计时器
        this.getList().then(() => {
          this.handleTime()
        })
      }
    },
    data: function () {
      return {
        time: 15,
        timer: undefined
      }
    }
